// Math.round() static method returns the value of a number rounded to the nearest integer
var x = Math.round(4.6);
var y = Math.round(4.4);
console.log(x);
console.log(y);





// The Math.ceil() method rounds a number rounded UP to the nearest integer.
var ceil1 = Math.ceil(4.4);
var ceil2 = Math.ceil(4.5);
console.log(ceil1);
console.log(ceil2);






// The Math.floor() method rounds a number DOWN to the nearest integer.
var floor1 = Math.floor(4.4);
var floor2 = Math.floor(4.5);
console.log(floor1);
console.log(floor2);





// The Math.trunc() method returns the integer part of a number.
// The Math.trunc() method removes the decimals (does NOT round the number).
var trunc=Math.trunc(4.125);
console.log(trunc);





// The Math.pow() method returns the value of x to the power of y (xy).
var pow=Math.pow(2,4);
var pow2=Math.pow(10,4);
console.log(pow);
console.log(pow2);





// The Math.sqrt() method returns the square root of a number.
var sqrt1=Math.sqrt(4);
var sqrt2=Math.sqrt(81);
console.log(sqrt1);
console.log(sqrt2);





// The Math.min() method returns the number with the lowest value.
var min=Math.min(1,2,3,4,5,6,7,-9);
console.log(min);






// The Math.max() method returns the number with the highest value.
var max=Math.max(1,2,3,4,5,6,7,-9);
console.log(max);







